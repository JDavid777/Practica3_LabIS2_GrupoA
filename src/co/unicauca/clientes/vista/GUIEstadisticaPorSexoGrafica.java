    @Override
    public void update(Observable obs, Object obj) {
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("Profesi√≥n: " + cliDB.getProfesion());
        graficar(cliDB.getTotalHombres(), cliDB.getTotalMujeres());
    }

    private void graficar(int totalHombres, int totalMujeres) {
        double porcHombres, porcMujeres;
        int total=totalHombres+totalMujeres;
        //DecimalFormat usado para darle un formato a la cantidad de decimaes en un double.
        DecimalFormat df = new DecimalFormat("#.##");
        porcHombres=((double)totalHombres*100)/(double)total;
        
        porcMujeres=((double)totalMujeres*100)/(double)total;
       
        this.txtHombres.setText(df.format(porcHombres)+"%");
        this.txtMujeres.setText(df.format(porcMujeres)+"%");
        this.barHombres.setValue((int)porcHombres);
        this.barMujeres.setValue((int)porcMujeres);
    }
